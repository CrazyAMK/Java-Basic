    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int numero = Integer.parseInt(txtNumero.getValue().toString());
        
        int restoDiv =  numero%2;
        lblRestoNum.setText(Integer.toString(restoDiv));
        
        float elCubo = (float) Math.pow(numero, 3);
        lblCuboNum.setText(Float.toString(elCubo));
        
        float raizQuad = (float) Math.sqrt(numero);
        lblQuadradaNum.setText(Float.toString(raizQuad));
        
        float cubica = (float) Math.cbrt(numero);
        lblCubicaNum.setText(Float.toString(cubica));
        
        int vAbsoluto = Math.abs(numero);
        lblAbsolutoNum.setText(Integer.toString(vAbsoluto));
        
        
        lblResto.setText("Resto da Divis√£o por 2");
        lblCubo.setText("Elevado ao Cubo");
        lblQuadrada.setText("Raiz Quadrada");
        lblCubica.setText("Raiz Cubica");
        lblAbsoluto.setText("Valor Absoluto");
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

